%% @doc
%% Mathematical functions.

% Module name.
-module(mth).

-export([sigmoid/1, sigmoid_der/1]).

%---------------------------------------------------------------------------------------------------
% Functions.
%---------------------------------------------------------------------------------------------------

%% @doc
%% Sigmoid function.
%%   X - argument.
sigmoid(X) ->
    1.0 / (1.0 + math:exp(-X)).

%---------------------------------------------------------------------------------------------------

%% @doc
%% Sigmoid derivative.
%%   X - argument.
sigmoid_der(X) ->
    sigmoid(X) * (1.0 - sigmoid(X)).

%---------------------------------------------------------------------------------------------------
